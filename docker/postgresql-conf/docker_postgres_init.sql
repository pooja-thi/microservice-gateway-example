CREATE USER gateway WITH PASSWORD 'gateway' CREATEDB;
CREATE DATABASE "gateway"
    WITH
    OWNER = gateway
    ENCODING = 'UTF8'
    LC_COLLATE = 'en_US.utf8'
    LC_CTYPE = 'en_US.utf8'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1;

